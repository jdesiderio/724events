import { useState } from "react";
import EventCard from "../../components/EventCard";
import Select from "../../components/Select";
import { useData } from "../../contexts/DataContext";
import Modal from "../Modal";
import ModalEvent from "../ModalEvent";

import "./style.css";

const PER_PAGE = 9;
let filteredEvents = [];
let typeSave;
let typeList = [];

const EventList = () => {
  const { data, error } = useData();
  const [type, setType] = useState();
  const [currentPage, setCurrentPage] = useState(1);
  filteredEvents = filteredEvents && filteredEvents.length > 0 && typeSave === type ? filteredEvents : data?.events?.filter((event) => {
  if(!type || event.type === type) {
    return true;
  }
    return false;
  }).filter((event, index) => {
    if (
      (currentPage - 1) * PER_PAGE <= index &&
      PER_PAGE * currentPage > index
    ) {
      return true;
    }
    return false;
  });
  
  const changeType = (evtType) => {
    setCurrentPage(1);
    setType(evtType);
  };
  const pageNumber = Math.floor((filteredEvents?.length || 0) / PER_PAGE) + 1;
  typeList = typeList.length > 0
  ? typeList
  : Array.from(
      new Set(data?.events.map((event) => event.type))
    ).sort();
  typeSave = type;
  return (
    <>
      {error && <div>An error occured</div>}
      {data === undefined || data?.events === undefined || data === null ? (
        "loading"
      ) : (
        <>
          <h3 className="SelectTitle">Cat√©gories</h3>
          <Select
            selection={Array.from(typeList)}
            onChange={(value) => (value ? changeType(value) : changeType(null))}
          />
          <div id="events" className="ListContainer">
          {filteredEvents && Array.isArray(filteredEvents) ? (
            filteredEvents.map((event) => (
              <Modal key={event.id} Content={<ModalEvent event={event} />}>
                {({ setIsOpened }) => (
                  <EventCard
                    onClick={() => setIsOpened(true)}
                    imageSrc={event.cover}
                    title={event.title}
                    date={new Date(event.date)}
                    label={event.type}
                  />
                )}
              </Modal>
            ))
          ) : (
            <div>{error}</div>
          )}
          </div>
          <div className="Pagination">
            {[...Array(pageNumber || 0)].map((_, n) => (
              // eslint-disable-next-line react/no-array-index-key
              <a key={n} href="#events" onClick={() => setCurrentPage(n + 1)}>
                {n + 1}
              </a>
            ))}
          </div>
        </>
      )}
    </>
  );
};

export default EventList;
